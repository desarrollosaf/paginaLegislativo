        <p data-aos="fade-up-left" data-aos-delay="100">El pasado 23 de abril, la LXI Legislatura mexiquense aprobó por unanimidad la nueva Ley de Movilidad y Seguridad Vial, la cual sienta las bases para mejorar los servicios e infraestructura de transporte público y vialidad para que la administración pública estatal cuente con principios y directrices para planear, regular, gestionar, fomentar y garantizar la movilidad.</p>
        <p data-aos="fade-up-right" data-aos-delay="200">La reforma fue enriquecida con 10 iniciativas de todas las bancadas parlamentarioas y al participación de la población, sector transportista, academia, especialistas y ciudadanía en general. Además, implicó un trabajo de más de año y medio, la realización de cuatro foros y la consulta a personas con discapacidad.</p>
        <p data-aos="fade-up-left" data-aos-delay="300">Dentro de sus consideraciones, detalla que la movilidad se gestionará para transitar hacia la sustentabilidad, en tanto que la seguridad vial es la prioridad del sistema integral de movilidad, mediante condiciones de accesibilidad, eficiencia, sostenibilidad, calidad, inclusión e igualdad.</p>
        <p data-aos="fade-up-right" data-aos-delay="400">En su elaboración se consideraron las iniciativas presentadas por los diputados y diputada de la bancada de Morena: Nazario Gutiérrez Martínez, Rosa María Zetina González, Adrián Manuel Galicia Salceda, Camilo Murillo Zavala y Dionicio Jorge García Sánchez.</p>
        <p data-aos="fade-up-left" data-aos-delay="500">Además, enriqueceieron la ley las iniciativas de los legisladores David Parra Sánchez y Jesús Gerardo Izquierdo Rojas, del PRI; de la legisladora Ingrid Schemelensky Castro y los legisladores Enrique Vargas del Villar y Francisco Brian Rojas, de la bancada del PAN; del diputado Omar Ortega Álvarez y las diputadas María Elida Castelán Mondragón (actualmente del PRI) y Viridiana Fuentes Cruz, del PRD; de las legisladoras María Luisa Mendoza Mondragón y Claudia Desireee Morales Robledo, de la bancada del PVEM; y de la diputada Juana Bonilla Jaime y el diputado Martín Zepeda Hernández, de la bancada de MC.</p>

